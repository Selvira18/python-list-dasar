tupA= (1,2,3,4)#Menampilkan 4 bilangan bulat positif pertama
print ("menampilkan tupA :",tupA)
tupB= (5,6,7,8)#Menampilkan 4 bilangan bulat positif berikutnya
print ("Menampilkan tupB :",tupB)
tupC=tupA+tupB#Menghubungkan tupA dan tupB
tupD=tupC*2#tupD salinan dari tupC
print ("Menampilkan tupD :",tupD)
print ("Mencetak element ke-3 tupD :",tupD[3])
print ("mencetak 3 element terakhir tupD :",tupD[-3:])
print ("mencetak panjang tupD :",len(tupD))
